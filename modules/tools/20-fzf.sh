#!/usr/bin/env bash

# =========================================================
# Plugin Metadata
# =========================================================

name="fzf"

description="Command-line fuzzy finder"

# =========================================================
# Verificar instalación
# =========================================================

check() {

    [[ -d "${HOME}/.fzf" ]]
}

# =========================================================
# Instalación
# =========================================================

install() {

    echo "[INFO] Instalando fzf"

    # -----------------------------------------------------
    # Dependencias
    # -----------------------------------------------------

    install_apt_packages git

    # -----------------------------------------------------
    # Clonar repositorio
    # -----------------------------------------------------

    git clone --depth 1 \
        https://github.com/junegunn/fzf.git \
        "${HOME}/.fzf"

    # -----------------------------------------------------
    # Ejecutar instalador
    # -----------------------------------------------------

    "${HOME}/.fzf/install" \
        --all
}
